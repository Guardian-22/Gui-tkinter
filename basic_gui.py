from  tkinter import *
"""
Creates the main application window (often called the "root window").
Tk() is a class in the tkinter module and is the entry point for any Tkinter application.
"""
root=Tk()

"""
Starts the event loop, which waits for user interactions like button clicks or keypresses.
This loop keeps the GUI running until the user closes the window.
"""
root.mainloop()
